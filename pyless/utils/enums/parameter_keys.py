class ParameterKeys:
    """
    all keys for parameters that a can be passed into a lambda. we set them up here so that there is no miss spelling
    or parameter, and so we dont have to always look at the lambda to see what the parameter key is.
    """
    AMOUNT = "amount"
    INTEREST_RATE = "interest_rate"
    LENGTH_OF_LOAN = "length_of_loan"
    MONTHLY_PAYMENT = "monthly_payment"
    STATUS = "status"
    U_ID = 'id'
